// --- Complex field types ---

// Verifies that a JSON document with all supported field types (string, array,
// nested map, boolean, integer, float, null) survives a round-trip through
// jsonToFirestoreFields and firestoreFieldsToJSON with values preserved.
func TestDocumentResource_ComplexFields(t *testing.T) {
	// Test that nested objects, arrays, booleans, nulls all round-trip correctly
	inputJSON := `{
		"name": "test",
		"tags": ["admin", "user"],
		"profile": {"bio": "hello", "scores": [1, 2, 3]},
		"active": true,
		"count": 42,
		"rating": 4.5,
		"nothing": null
	}`

	firestoreFields, err := jsonToFirestoreFields(inputJSON)
	if err != nil {
		t.Fatalf("jsonToFirestoreFields error: %v", err)
	}

	// Simulate what the server returns (the same Firestore fields)
	resultJSON, err := firestoreFieldsToJSON(firestoreFields)
	if err != nil {
		t.Fatalf("firestoreFieldsToJSON error: %v", err)
	}

	var original, result map[string]interface{}
	json.Unmarshal([]byte(inputJSON), &original)
	json.Unmarshal([]byte(resultJSON), &result)

	// Verify key fields
	if result["name"] != "test" {
		t.Errorf("name = %v", result["name"])
	}
	if result["active"] != true {
		t.Errorf("active = %v", result["active"])
	}
	if result["nothing"] != nil {
		t.Errorf("nothing = %v, want nil", result["nothing"])
	}

	tags := result["tags"].([]interface{})
	if len(tags) != 2 || tags[0] != "admin" {
		t.Errorf("tags = %v", tags)
	}

	profile := result["profile"].(map[string]interface{})
	if profile["bio"] != "hello" {
		t.Errorf("profile.bio = %v", profile["bio"])
	}
	scores := profile["scores"].([]interface{})
	if len(scores) != 3 {
		t.Errorf("profile.scores length = %d, want 3", len(scores))
	}
}

