// --- Create ---

// Verifies creating a document with an explicit ID sends a POST request to the
// correct collection path with a documentId query parameter, submits Firestore-
// formatted fields in the request body, and parses the response document ID and
// timestamps correctly.
func TestDocumentResource_Create_WithExplicitID(t *testing.T) {
	var capturedMethod string
	var capturedPath string
	var capturedQuery string
	var capturedBody map[string]interface{}

	server := httptest.NewServer(http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		capturedMethod = r.Method
		capturedPath = r.URL.Path
		capturedQuery = r.URL.RawQuery
		body, _ := io.ReadAll(r.Body)
		json.Unmarshal(body, &capturedBody)

		resp := firestoreDocumentResponse(
			"projects/test-project/databases/(default)/documents/users/user-123",
			map[string]interface{}{
				"name":  map[string]interface{}{"stringValue": "Alice"},
				"email": map[string]interface{}{"stringValue": "alice@example.com"},
			},
		)
		w.Header().Set("Content-Type", "application/json")
		json.NewEncoder(w).Encode(resp)
	}))
	defer server.Close()

	r := &DocumentResource{client: newTestClient(server)}

	// Simulate a create by calling the HTTP logic directly.
	// We test the URL construction and request body by examining captured values.
	reqURL := "https://firestore.googleapis.com/v1/projects/test-project/databases/(default)/documents/users?documentId=user-123"

	firestoreFields, err := jsonToFirestoreFields(`{"name":"Alice","email":"alice@example.com"}`)
	if err != nil {
		t.Fatal(err)
	}

	bodyMap := map[string]interface{}{"fields": firestoreFields}
	bodyBytes, _ := json.Marshal(bodyMap)

	httpReq, _ := http.NewRequest("POST", reqURL, strings.NewReader(string(bodyBytes)))
	httpReq.Header.Set("Content-Type", "application/json")
	httpResp, err := r.client.HTTPClient.Do(httpReq)
	if err != nil {
		t.Fatalf("request error: %v", err)
	}
	defer httpResp.Body.Close()

	if capturedMethod != "POST" {
		t.Errorf("method = %s, want POST", capturedMethod)
	}
	if capturedPath != "/v1/projects/test-project/databases/(default)/documents/users" {
		t.Errorf("path = %s", capturedPath)
	}
	if !strings.Contains(capturedQuery, "documentId=user-123") {
		t.Errorf("query = %s, want documentId=user-123", capturedQuery)
	}

	// Verify request body has Firestore-formatted fields
	fields := capturedBody["fields"].(map[string]interface{})
	nameField := fields["name"].(map[string]interface{})
	if nameField["stringValue"] != "Alice" {
		t.Errorf("name = %v, want Alice", nameField)
	}

	// Verify response parsing
	respBody, _ := io.ReadAll(httpResp.Body)
	var doc FirestoreDocument
	json.Unmarshal(respBody, &doc)

	if extractDocumentID(doc.Name) != "user-123" {
		t.Errorf("document_id = %s, want user-123", extractDocumentID(doc.Name))
	}
	if doc.CreateTime != "2024-01-01T00:00:00Z" {
		t.Errorf("createTime = %s", doc.CreateTime)
	}
}

