// Verifies the full import format "project/database/collection/document_id"
// is correctly parsed into its four components.
func TestDocumentResource_ImportState_FullFormat(t *testing.T) {
	// Test parsing of "project/database/collection/document_id"
	parts := strings.Split("my-project/(default)/users/user-123", "/")

	if len(parts) < 4 {
		t.Fatal("expected at least 4 parts")
	}

	project := parts[0]
	database := parts[1]
	collection := strings.Join(parts[2:len(parts)-1], "/")
	documentID := parts[len(parts)-1]

	if project != "my-project" {
		t.Errorf("project = %s, want my-project", project)
	}
	if database != "(default)" {
		t.Errorf("database = %s, want (default)", database)
	}
	if collection != "users" {
		t.Errorf("collection = %s, want users", collection)
	}
	if documentID != "user-123" {
		t.Errorf("document_id = %s, want user-123", documentID)
	}
}

